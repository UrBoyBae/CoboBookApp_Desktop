    private void searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKeyReleased
        // TODO add your handling code here:
        String srcMethod = kategori.getSelectedItem().toString();
        String searchEngine = search.getText();

        if ("Kode Buku".equals(srcMethod)) {
            try {
                tabModel = (DefaultTableModel) tabel.getModel();
                tabModel.setRowCount(0);
                k.setDriver();
                k.query = "select * from databuku where kd_buku like '%" + searchEngine + "%'";
                k.read();

                while (k.rs.next()) {
                    a1 = k.rs.getString("kd_buku");
                    b1 = k.rs.getString("judul_buku");
                    c1 = k.rs.getString("penulis");
                    d1 = k.rs.getString("penerbit");
                    e1 = k.rs.getString("thn_terbit");
                    f1 = k.rs.getString("stok");
                    String[] data = {a1, b1, c1, d1, e1, f1};
                    tabModel.addRow(data);
                }

            } catch (Exception e) {
                System.out.println("Error" + e);
            }

        } else if ("Judul Buku".equals(srcMethod)) {
            try {
                tabModel = (DefaultTableModel) tabel.getModel();
                tabModel.setRowCount(0);
                k.setDriver();
                k.query = "select * from databuku where judul_buku like '%" + searchEngine + "%'";
                k.read();

                while (k.rs.next()) {
                    a1 = k.rs.getString("kd_buku");
                    b1 = k.rs.getString("judul_buku");
                    c1 = k.rs.getString("penulis");
                    d1 = k.rs.getString("penerbit");
                    e1 = k.rs.getString("thn_terbit");
                    f1 = k.rs.getString("stok");
                    String[] data = {a1, b1, c1, d1, e1, f1};
                    tabModel.addRow(data);
                }

            } catch (Exception e) {
                System.out.println("Error" + e);
            }
        } else {
            showData();
        }
    }//GEN-LAST:event_searchKeyReleased

