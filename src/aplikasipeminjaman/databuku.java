    void showData() {
        try {
            tabModel = (DefaultTableModel) tabel.getModel();
            tabModel.setRowCount(0);
            k.setDriver();
            k.query = "select * from databuku";
            k.read();
            while (k.rs.next()) {
                a = k.rs.getString("kd_buku");
                b = k.rs.getString("judul_buku");
                c = k.rs.getString("penulis");
                d = k.rs.getString("penerbit");
                e = k.rs.getString("thn_terbit");
                f = k.rs.getString("stok");

                String[] data = {a, b, c, d, e, f};
                tabModel.addRow(data);
            }
        } catch (Exception ex) {
            System.out.println("Error" + ex);
        }
    }

