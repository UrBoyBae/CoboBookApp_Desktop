    public void autoNumber(){
        try {
            Connection koneksi = DriverManager.getConnection("jdbc:mysql://localhost:3306/cobobook","root","");
            Statement stat = koneksi.createStatement();
            ResultSet rs = stat.executeQuery("SELECT kd_pinjam FROM peminjam ORDER BY kd_pinjam DESC");
            if (rs.next()) {
                String kode = rs.getString("kd_pinjam").substring(2);
                String AN = "" +(Integer.parseInt(kode)+1);
                String Nol = "";
                
                if(AN.length()==1){
                Nol = "00";
                } else if(AN.length()==2){
                Nol = "0";
                } else if(AN.length()==3){
                Nol = "";
                } else if(AN.length()==4) {
                Nol = "";
                } else if(AN.length()==5) {
                Nol = "";
                }
                
                kdpinjam.setText("PB" + Nol + AN);
                
            } else {
                kdpinjam.setText("PB001");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

