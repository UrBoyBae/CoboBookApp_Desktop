    void create() {
        insPinjam = String.valueOf(date.format(tglpinjam.getDate()));
        insKembali = String.valueOf(date.format(tglkembali.getDate()));
        insKDPinjam = kdpinjam.getText();
        insNama = namapinjam.getText();
        insAlamat = alamatpinjam.getText();
        insKDBuku = kdbuku.getText();
        insJbuku = judulbuku.getText();
        insJml = jmlbuku.getText();
        }

    protected void clearForm(){
        namapinjam.setText("");
        alamatpinjam.setText("");
        kdbuku.setText("");
        judulbuku.setText("");
        jmlbuku.setText("");
        tglpinjam.setCalendar(null);
        tglkembali.setCalendar(null);
        }

    

    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        if ((namapinjam.getText().isEmpty())&& (alamatpinjam.getText().isEmpty()) && (kdbuku.getText().isEmpty())
                && (judulbuku.getText().isEmpty()) && (jmlbuku.getText().isEmpty()) && ((tglpinjam.getDate().toString()).isEmpty())
                && ((tglkembali.getDate().toString()).isEmpty())) {
            JOptionPane.showMessageDialog(null, "Isi Data Terlebih Dahulu", "info", JOptionPane.WARNING_MESSAGE);
        }
        else if ((namapinjam.getText().isEmpty())|| (alamatpinjam.getText().isEmpty()) || (kdbuku.getText().isEmpty())
                || (judulbuku.getText().isEmpty()) && (jmlbuku.getText().isEmpty()) || ((tglpinjam.getDate().toString()).isEmpty())
                || ((tglkembali.getDate().toString()).isEmpty())) {
            JOptionPane.showMessageDialog(null, "lengkapi Data Terlebih Dahulu", "info", JOptionPane.WARNING_MESSAGE);
        }
        else {
            try {
                create();
                k.setDriver();
                k.query = "insert into peminjam values(?,?,?,?,?,?,?,?)";
                k.CUD();
                k.ps.setString(1, insKDPinjam);
                k.ps.setString(2, insNama);
                k.ps.setString(3, insAlamat);
                k.ps.setString(4, insKDBuku);
                k.ps.setString(5, insJbuku);
                k.ps.setString(6, insJml);
                k.ps.setString(7, insPinjam);
                k.ps.setString(8, insKembali);

                k.ps.execute();
                JOptionPane.showMessageDialog(null, "Data Tersimpan","Info", JOptionPane.INFORMATION_MESSAGE);
                autoNumber();


            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Data Gagal Tersimpan");
                System.out.println("Error" + ex);
            }

            clearForm();
        }
    }//GEN-LAST:event_simpanActionPerformed

