    void showData() {
        try {
            tabModel = (DefaultTableModel) tabel.getModel();
            tabModel.setRowCount(0);
            k.setDriver();
            k.query = "SELECT * FROM peminjam WHERE kd_pinjam NOT IN (SELECT kd_pinjam FROM pengembalian);";
            k.read();
            while (k.rs.next()) {
                a = k.rs.getString("kd_pinjam");
                b = k.rs.getString("nama_peminjam");
                c = k.rs.getString("alamat");
                d = k.rs.getString("kd_buku");
                e = k.rs.getString("judul_buku");
                f = k.rs.getString("jumlah_buku");
                g = k.rs.getString("tgl_pinjam");
                h = k.rs.getString("tgl_kembali");

                String[] data = {a, b, c, d, e, f, g, h};
                tabModel.addRow(data);
            }
        } catch (Exception ex) {
            System.out.println("Error" + ex);
        }
    }

