    private void searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKeyReleased
        // TODO add your handling code here:
        String srcMethod = kategori.getSelectedItem().toString();
        String searchEngine = search.getText();

        if ("Kode Pinjam".equals(srcMethod)) {
            try {
                tabModel = (DefaultTableModel) tabel.getModel();
                tabModel.setRowCount(0);
                k.setDriver();
                k.query = "select * from peminjam where kd_pinjam like '%" + searchEngine + "%'";
                k.read();

                while (k.rs.next()) {
                    a1 = k.rs.getString("kd_pinjam");
                    b1 = k.rs.getString("nama_peminjam");
                    c1 = k.rs.getString("alamat");
                    d1 = k.rs.getString("kd_buku");
                    e1 = k.rs.getString("judul_buku");
                    f1 = k.rs.getString("jumlah_buku");
                    g1 = k.rs.getString("tgl_pinjam");
                    h1 = k.rs.getString("tgl_kembali");

                    String[] data = {a1, b1, c1, d1, e1, f1, g1, h1};
                    tabModel.addRow(data);
                }

            } catch (Exception e) {
                System.out.println("Error" + e);
            }

        } else if ("Nama Peminjam".equals(srcMethod)) {
            try {
                tabModel = (DefaultTableModel) tabel.getModel();
                tabModel.setRowCount(0);
                k.setDriver();
                k.query = "select * from peminjam where nama_peminjam like '%" + searchEngine + "%'";
                k.read();

                while (k.rs.next()) {
                    a1 = k.rs.getString("kd_pinjam");
                    b1 = k.rs.getString("nama_peminjam");
                    c1 = k.rs.getString("alamat");
                    d1 = k.rs.getString("kd_buku");
                    e1 = k.rs.getString("judul_buku");
                    f1 = k.rs.getString("jumlah_buku");
                    g1 = k.rs.getString("tgl_pinjam");
                    h1 = k.rs.getString("tgl_kembali");

                    String[] data = {a1, b1, c1, d1, e1, f1, g1, h1};
                    tabModel.addRow(data);
                }

            } catch (Exception e) {
                System.out.println("Error" + e);
            }
        } else {
            showData();
        }
    }//GEN-LAST:event_searchKeyReleased

