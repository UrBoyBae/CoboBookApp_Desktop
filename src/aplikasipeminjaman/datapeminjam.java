    private void editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editMouseClicked
        // TODO add your handling code here:
        tabModel = (DefaultTableModel) tabel.getModel();

        //cek jika row sudah terisi data minimal ada 1 data
        if (tabel.getSelectedRowCount() == 1) {
            m = kdpinjam.getText();
            n = namapinjam.getText();
            x = alamatpinjam.getText();
            o = kdbuku.getText();
            i = judulbuku.getText();
            j = jmlbuku.getText();

            int q = JOptionPane.showConfirmDialog(null, "Yakin Ingin Mengubah Data Ini?", "Warning", JOptionPane.YES_NO_OPTION);

            if (q == JOptionPane.YES_OPTION) {
                try {
                    k.setDriver();
                    k.query = "update peminjam set kd_pinjam='" + m + "',nama_peminjam='" + n + "',alamat='" + x + "',kd_buku='" + o + "',judul_buku='" + i + "',jumlah_buku='" + j + "' where kd_pinjam='" + m + "'";
                    k.CUD();
                    k.ps.executeUpdate();
                } catch (Exception e) {
                    System.err.println("error" + e);
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Tidak ada data yang dipilih", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        clearForm();
        showData();
    }//GEN-LAST:event_editMouseClicked

