public void autoNumber(){
        try {
            Connection koneksi = DriverManager.getConnection("jdbc:mysql://localhost:3306/cobobook","root","");
            Statement stat = koneksi.createStatement();
            ResultSet rs = stat.executeQuery("SELECT kd_buku FROM databuku ORDER BY kd_buku DESC");
            if (rs.next()) {
                String kode = rs.getString("kd_buku").substring(2);
                String AN = "" +(Integer.parseInt(kode)+1);
                String Nol = "";
                
                if(AN.length()==1){
                Nol = "00";
                } else if(AN.length()==2){
                Nol = "0";
                } else if(AN.length()==3){
                Nol = "";
                } else if(AN.length()==4) {
                Nol = "";
                } else if(AN.length()==5) {
                Nol = "";
                }
                kdbuku.setText("SB" + Nol + AN);
                
            } else {
                kdbuku.setText("SB001");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
