    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        if ((judulbuku.getText().isEmpty())&& (penulis.getText().isEmpty()) && (penerbit.getText().isEmpty())
                && (thnterbit.getText().isEmpty()) && (stok.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Isi Data Terlebih Dahulu", "info", JOptionPane.WARNING_MESSAGE);
        }
        else if ((judulbuku.getText().isEmpty())|| (penulis.getText().isEmpty()) || (penerbit.getText().isEmpty())
                || (thnterbit.getText().isEmpty()) && (stok.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "lengkapi Data Terlebih Dahulu", "info", JOptionPane.WARNING_MESSAGE);
        }
        else {
            try {
                create();
                k.setDriver();
                k.query = "insert into databuku values(?,?,?,?,?,?)";
                k.CUD();
                k.ps.setString(1, insKDbuku);
                k.ps.setString(2, insJudul);
                k.ps.setString(3, insPenulis);
                k.ps.setString(4, insPenerbit);
                k.ps.setString(5, insTahun);
                k.ps.setString(6, insStok);

                k.ps.execute();
                JOptionPane.showMessageDialog(null, "Data Tersimpan","Info", JOptionPane.INFORMATION_MESSAGE);
                autoNumber();


            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Data Gagal Tersimpan");
                System.out.println("Error" + ex);
            }

            clearForm();
        }
        
    }//GEN-LAST:event_simpanActionPerformed

